function ToolButton({ children, icon, asLabel = false, onChange }) {
  const content = (
    <span className="_feed_inner_text_area_bottom_photo_link">
      <span className="_feed_inner_text_area_bottom_photo_iamge _mar_img">{icon}</span>
      {children}
      {asLabel ? <input className="feed-file-input" type="file" accept="image/*" onChange={onChange} /> : null}
    </span>
  )

  return <div className="_feed_inner_text_area_bottom_photo _feed_common">{asLabel ? <label>{content}</label> : <button type="button" className="feed-tool-button">{content}</button>}</div>
}

export default function PostComposer({ form, setForm, submitting, onSubmit }) {
  const hasText = form.body.trim().length > 0

  return (
    <form className="_feed_inner_text_area _b_radious6 _padd_b24 _padd_t24 _mar_b16" onSubmit={onSubmit}>
      <div className="_feed_inner_text_area_box _padd_r24 _padd_l24">
        <div className="_feed_inner_text_area_box_image">
          <img src="/assets/images/txt_img.png" alt="" className="_txt_img" />
        </div>
        <div className={`form-floating _feed_inner_text_area_box_form${hasText ? ' feed-has-text' : ''}`}>
          <textarea
            className="form-control _textarea"
            placeholder="Write something ..."
            id="feedComposerTextarea"
            value={form.body}
            onChange={(event) => setForm((current) => ({ ...current, body: event.target.value }))}
            aria-label="Write a new post"
          />
          <label className="_feed_textarea_label" htmlFor="feedComposerTextarea">Write something ...</label>
        </div>
      </div>
      <div className="_feed_inner_text_area_bottom">
        <div className="_feed_inner_text_area_item">
          <ToolButton icon={<PhotoIcon />} asLabel onChange={(event) => setForm((current) => ({ ...current, image: event.target.files?.[0] || null }))}>Photo</ToolButton>
          <ToolButton icon={<VideoIcon />}>Video</ToolButton>
          <ToolButton icon={<EventIcon />}>Event</ToolButton>
          <ToolButton icon={<ArticleIcon />}>Article</ToolButton>
        </div>
        <select className="feed-visibility-select" value={form.visibility} onChange={(event) => setForm((current) => ({ ...current, visibility: event.target.value }))} aria-label="Post visibility">
          <option value="public">Public</option>
          <option value="private">Private</option>
        </select>
        <div className="_feed_inner_text_area_btn">
          <button type="submit" className="_feed_inner_text_area_btn_link" disabled={submitting}>
            <SendIcon />
            <span>{submitting ? 'Posting...' : 'Post'}</span>
          </button>
        </div>
      </div>
      {form.image ? <small className="feed-selected-file">Selected: {form.image.name}</small> : null}
    </form>
  )
}
